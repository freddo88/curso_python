mascotas = ["Firulais", "Santa", "Luna", "Miel"]
print(mascotas[1])  # acedemos a un elemento de la lista
mascotas[1] = "Copito"  # cambiamos un elemento de la lista
print(mascotas)
print(mascotas[0:2])  # imprimimos un parcial de la lista
print(mascotas[-1])  # imprimimos el ultimo elemento de la lista
print(mascotas[::2])  # imprime los elementos de dos en dos
