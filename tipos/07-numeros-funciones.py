import math  # modulo de funciones matematicas

print(round(1.3))  # redondea al numero eentero mas cercano
print(abs(-66))  # vlaor obsoluto del numero

print(math.ceil(1.1))  # lo aproxima al entero mayor
print(math.floor(1.1))  # lo aproxima al entero menor
print(math.isnan(23))  # devuelve true o flase si el argumento no es un numero
print(math.pow(10, 2))  # elevar un numero
print(math.sqrt(9))  # raiz cuadrada
